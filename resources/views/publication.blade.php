Punjabi University, Patiala, one of the leading premier institutions in the northern region of India, came into 
existence on 30th April, 1962 under Punjab Act No. 35 of 1961. This is the only University in the country to 
be named after a language. It was established primarily for the development and enrichment of Punjabi 
language and culture. However, with the passage of time, the University started contributing significantly in 
other disciplines of education also. It was for its splendid and extraordinary all round contribution that the
 University won the Five Star status from NAAC, an autonomous body of the UGC.<br><br>
 <font color="blue>BUREAU AT A GLANCE</font><br>
 Publication Bureau, an important wing of the University, was established in 1966 to further the cause of 
 Punjabi language, literature and culture. Entrusted with the task of publishing books and research journals 
 commissioned by the University, the Bureau takes the pride to mention that more than three thousand titles 
 covering a number of subjects in English, Punjabi and other language are in its kitty. It has been contributing 
 constantly towards the promotion of books and developing reading habit among the masses. A wide variety 
 of quality books ranging from scriptures to the works of fiction books on medical and engineering sciences are 
 made available to the readers of all age groups at a reasonable price.
Over the years, the Bureau’s publications have earned a distinctive name for their content and excellent 
quality of production. The Bureau won the National Award twice for its excellence in Printing and Designing 
during the years 1965 and 1971. From 2011-12 to 2014-2015 it got Awards for Excellence in Publishing from 
the Federation of Indian Publishers for its publications, “Gurushabad Ratnakar Mahan Kosh” (Punjabi) and 
other books in the reference book category of regional language. The number of awards won by the Bureau 
from time to time establish its claim.<br><br>
<font color="blue>Our Vision</font><br>
To make available quality books for higher education at a low cost, and to inculcate a habit of reading and 
buying books among more and more people. 
<font color="blue">Kitab Ghar</font><br>
The idea for the establishment of an air-conditioned ‘Kitab Ghar’, an outlet for the display and sale of 
University publications at the University Campus, was mooted out by the worthy Vice-Chancellor, Dr. Jaspal 
Singh himself which shows his deep love for the books and concern about the readers. It remains open on all 
days of the week except Sunday from 8:30 a.m to 8:00 p.m.<br>
<font color="blue">Organizing Book Fairs</font><br>
Organizing/Participating in book fairs/exhibitions at the local, regional and national levels is another salient 
feature of the Bureau. A mobile van is meant for this purpose only. It is an endeavour to make the books 
available to the people at their door steps.<br><br>
